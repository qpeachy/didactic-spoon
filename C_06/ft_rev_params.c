#include <unistd.h>
int	main(int argc, char **argv)
{
	int	i;
	int	j;
	(void)argc;
	j = 0;
	i = 0;
	while (argv[i])
	{
		i++;
	}
	while (i - 1 > 0)
	{
		j = 0;
		while (argv[i - 1][j])
		{
			write (1, &argv[i - 1][j], 1);
			j++;
		}
		i--;
		write (1, "\n", 1);
	}
}
