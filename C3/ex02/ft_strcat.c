char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	u;

	u = 0;
	i = 0;
	while (dest[u] != '\0')
	{
		u++;
	}
	while (src[i] != '\0')
	{
		dest[u + i] = src[i];
		i++;
	}
	dest[u + i] = '\0';
	return (dest);
}
