int	ft_str_is_uppercase(char *str)
{
	int	h;

	h = 0;
	if (str[h] == '\0')
		return (1);
	while (str[h] != '\0')
	{
		if (!(str[h] >= 65 && str[h] <= 90))
			return (0);
		h++;
	}
	return (1);
}
