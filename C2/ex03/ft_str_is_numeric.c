int	ft_str_is_numeric(char *str)
{
	int	j;

	j = 0;
	if (str[j] == '\0')
		return (1);
	while (str[j] != '\0')
	{
		if (!(str[j] >= 48 && str[j] <= 57))
			return (0);
		j++;
	}
	return (1);
}
