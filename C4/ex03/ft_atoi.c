int	ft_atoi(char *str)
{
	int	i;
	int	minus;
	int	nb;

	i = 0;
	minus = 0;
	nb = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			minus++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = (nb * 10) + (str[i] - '0');
		i++;
	}
	if ((minus % 2) != 0)
		nb = -nb;
	return (nb);
}
